package cn.edu.cqu.yihao.pojo;

public class IndentWithPost {
	private Indent indent = null;
	private Post post = null;
	private int havePost = 0;
	
	
	public Indent getIndent() {
		return indent;
	}
	public void setIndent(Indent indent) {
		this.indent = indent;
	}
	public Post getPost() {
		return post;
	}
	public void setPost(Post post) {
		this.post = post;
	}
	public int getHavePost() {
		return havePost;
	}
	public void setHavePost(int havePost) {
		this.havePost = havePost;
	}
	
}
